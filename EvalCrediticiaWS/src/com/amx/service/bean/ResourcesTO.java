package com.amx.service.bean;

public class ResourcesTO {
	
	 private int codeResource;
	 private String codeAplication; 
	 private String resourceName; 
	 private String valor; 
	 
	 
	 public ResourcesTO(){	 
	 }


	/**
	 * @return the codeResource
	 */
	public int getCodeResource() {
		return codeResource;
	}


	/**
	 * @param codeResource the codeResource to set
	 */
	public void setCodeResource(int codeResource) {
		this.codeResource = codeResource;
	}


	/**
	 * @return the codeAplication
	 */
	public String getCodeAplication() {
		return codeAplication;
	}


	/**
	 * @param codeAplication the codeAplication to set
	 */
	public void setCodeAplication(String codeAplication) {
		this.codeAplication = codeAplication;
	}


	/**
	 * @return the resourceName
	 */
	public String getResourceName() {
		return resourceName;
	}


	/**
	 * @param resourceName the resourceName to set
	 */
	public void setResourceName(String resourceName) {
		this.resourceName = resourceName;
	}


	/**
	 * @return the valor
	 */
	public String getValor() {
		return valor;
	}


	/**
	 * @param valor the valor to set
	 */
	public void setValor(String valor) {
		this.valor = valor;
	}
	 
	 
	 
}
